    /**************** First Form Get **********************/
    public function firstFormGet($id , $type)
    {
        $project = Project::findOrFail($id);
        if($project->product_first != null ){
            $type = 'first';
        }
        return view('expenses.form')->with('project' , $project)->with('type' , $type);
    }

    public function firstFormPost($id)
    {
        $project = Project::findOrFail($id);
        $route = redirect()->route('expenses.form',['project_id'=> $project->id , 'type' => 'first']);
        return $route;
    }
